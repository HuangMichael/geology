/**
 *  根据项目编号id获取项目
 * @param id 项目id
 * @param projects 项目列表
 */
function getProjectById(projects, id) {
    var res = [];
    for (var x in projects) {
        if (projects[x].id == (id)) {
            res.push(projects[x]);
        }
    }
    return res;

}

